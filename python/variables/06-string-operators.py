# 문자열 연산하기
# 파이썬에서는 문자열을 더하거나 곱할 수 있다. (다른 언어에서는 쉽게 찾아볼 수 없음)


# 🎈 문자열 더해서 연결하기 (Concatenation)
head = "Python"
tail = " is fun!"
print(head+tail) # Python is fun!


# 🎈 문자열 곱하기(반복)
print("ㅋ"*9) # ㅋㅋㅋㅋㅋㅋㅋㅋㅋ

# 문자열 곱하기 응용
print("="*50)
print("My program")
print("="*50)


# 🎈 문자열 길이 구하기 - len()
a = "Life is too short"
print(len(a)) # 17
b = "You need python"
print(len(b)) # 15
