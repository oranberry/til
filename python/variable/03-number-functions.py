# 숫자 처리 함수
# 🎈 abs() - 절댓값
print(abs(-5)) # 5

# 🎈 pow() - 제곱
print(pow(4, 2)) # 4^2 = 4*4 = 16

# 🎈 max() - 최댓값
print(max(5, 12)) # 12

# 🎈 min() - 최솟값
print(min(5, 12)) # 5

# 🎈 round() - 반올림
print(round(3.14)) # 3
print(round(4.99)) # 5


# math module 이용
from math import *
# 🎈 floor() - 내림
print(floor(4.99)) # 4

# 🎈ceil() - 올림
print(ceil(3.14)) # 4

# 🎈sqrt() - 제곱근
print(sqrt(16)) # 4