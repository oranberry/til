# 숫자형을 활용하기 위한 연산자
a = 7
b = 4

# 🎈 사칙연산
print(a+b)
print(a-b)
print(a*b)
print(a/b)


# 🎈 x의 y제곱을 나타내는 ** 연산자
print(a**b)


# 🎈 나눗셈 후 "나머지"를 반환하는 % 연산자
print(a%b)
print(b%a)


# 🎈 나눗셈 후 "몫"을 반환하는 // 연산자
print(a//b)
print(b//a)
print("-"*50)


# 🎈 Assignment Operator
number = 2 + 3 * 4
print(number) # 14
number = number + 2 
print(number) # 16
number += 2
print(number) # 18
number *= 2
print(number) # 36
number /= 2
print(number) # 18.0
number -= 2
print(number) # 16.0
number %= 5
print(number) # 1.0