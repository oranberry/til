// 💫 C++ Fundamental Types

// 👉🏻 bool - Boolean value, either true or false

#include <iostream>
using namespace std;

int main()
{
    // bool: 0(false) 혹은 1(true)
    // 0 이외의 모든 값은 1로 나타난다.
    bool a = 0;
    bool b = 1;
    bool c = 10;

    cout << a << " " << b << " " << c << endl; // 0 1 1
    return 0;
}