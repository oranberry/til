// ✨ Difference between array and structure ✨

//  ➰ Arrays
// 1. All elements of an array have the same type
// 2. To select an array element, we specify its position (as an integer subscript)
// 3. Arrays can't be copied using the = operator

// ➰ Structure
// A structure is a logical choice for storing a collection of related data items
// 1. The elements of a structure (its members, in C) aren't required to have the same type
// 2. The members of a structure have names; to select a particular member, we specify its name, not its position
// 3. Structure can be copied using = operator (-> 03-operation-structure.c )
// An array embedded within a structure is copied when the enclosing structure is copied.